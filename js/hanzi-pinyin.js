// 自动反转：汉字 → 拼音
const HANZI_TO_PINYIN = {};
(function buildReverse() {
  for (const [pinyin, chars] of Object.entries(PINYIN_TO_HANZI)) {
    for (const ch of chars) {
      // 第一个映射优先（多音字取最常见读音）
      if (!HANZI_TO_PINYIN[ch]) {
        HANZI_TO_PINYIN[ch] = pinyin;
      }
    }
  }
})();

/**
 * 将汉字文本转为拼音数组
 * @param {string} text - 汉字文本
 * @returns {string[]} - 每个字符对应的拼音（查不到的保留原字符）
 */
function lookupPinyin(text) {
  const result = [];
  for (const ch of text) {
    result.push(HANZI_TO_PINYIN[ch] || ch);
  }
  return result;
}
